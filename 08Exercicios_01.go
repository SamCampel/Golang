// Utilizando o operador curto de declaração, atribuia esses valores com identificadores "x", "y" e "z" - 42, José e true.
// Depois demonstre os valor em um único print e multiplos prints

package main

import "fmt"

func main() {

	x := 42
	y := "José"
	z := true

	fmt.Println(x, y, z)
	fmt.Println(x)
	fmt.Println(y)
	fmt.Println(z)

}
